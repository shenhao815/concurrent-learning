@Slf4j
public class DIYPool {
    public static void main(String[] args) {
        Pool pool = new Pool(2);
        for (int i = 0; i < 5; i++) {
            new Thread(() -> {
                Connection conn = pool.borrow();
                Sleeper.sleep(1000);
                pool.free(conn);
            }).start();
        }
    }
}
@Slf4j
class Pool{
    // 连接池大小
    private final int poolSize;
    // 连接池数组
    private Connection[] connections;
    // 连接池内每个连接的状态 0-空闲 1-被占用
    private AtomicIntegerArray state;

    public Pool(int poolSize) {
        this.poolSize = poolSize;
        this.connections = new Connection[poolSize];
        this.state = new AtomicIntegerArray(new int[poolSize]);
        for (int i = 0; i < poolSize; i++) {
            connections[i] = new MockConnection("连接" + (i + 1));
        }
    }

    // 借连接对象
    public Connection borrow(){
        while (true) {
            for (int i = 0; i < poolSize; i++) {
                if (state.get(i) == 0) {
                    state.compareAndSet(i, 0, 1);
                    log.debug("borrow {}",connections[i]);
                    return connections[i];
                }
            }
            /*
             * 此部分的代码可以省略，只靠上面的cas操作实现阻塞，但是这样做非常不好
             * 因为：cas内部使用了while(true)方式，所以它只适合短时间内的操作，
             * 而连接对象要操作数据库，非常占用时间，所以cas会让cpu一直空转，浪费性能；
             * 所以使用wait更好！
             */
            synchronized (this) {
                try {
                    log.debug("wait...");
                    this.wait();
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
        }
    }

    // 归还连接
    public void free(Connection conn){
        for (int i = 0; i < poolSize; i++) {
            if (connections[i] == conn) {
                state.set(i,0);
                synchronized (this) {
                    log.debug("free {}",connections[i]);
                    this.notifyAll();
                }
                break;
            }
        }

    }
}
